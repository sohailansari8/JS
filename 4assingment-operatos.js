// Assignment operators are used to assign values to variables.
// The most basic assignment operator is the '=' operator.

let a = 4;  // Assigning 4 to variable 'a'
console.log("a =", a); // Output: a = 4

// 1. Addition Assignment (+=)
// Adds the right operand to the left operand and assigns the result to the left operand.
a += 4;  // Equivalent to: a = a + 4
console.log("a += 4 =", a); // Output: a += 4 = 8

// 2. Subtraction Assignment (-=)
// Subtracts the right operand from the left operand and assigns the result to the left operand.
a -= 2;  // Equivalent to: a = a - 2
console.log("a -= 2 =", a); // Output: a -= 2 = 6

// 3. Multiplication Assignment (*=)
// Multiplies the left operand by the right operand and assigns the result to the left operand.
a *= 3;  // Equivalent to: a = a * 3
console.log("a *= 3 =", a); // Output: a *= 3 = 18

// 4. Division Assignment (/=)
// Divides the left operand by the right operand and assigns the result to the left operand.
a /= 2;  // Equivalent to: a = a / 2
console.log("a /= 2 =", a); // Output: a /= 2 = 9

// 5. Modulus Assignment (%=)
// Finds the remainder when the left operand is divided by the right operand and assigns it to the left operand.
a %= 5;  // Equivalent to: a = a % 5
console.log("a %= 5 =", a); // Output: a %= 5 = 4

// 6. Exponentiation Assignment (**=)
// Raises the left operand to the power of the right operand and assigns the result to the left operand.
a **= 2;  // Equivalent to: a = a ** 2
console.log("a **= 2 =", a); // Output: a **= 2 = 16

// Summary Table:
// | Operator | Example  | Equivalent To | Description |
// |----------|---------|---------------|-------------|
// | =  | a = b  | a = b  | Assigns `b` to `a` |
// | += | a += b | a = a + b | Adds `b` to `a` |
// | -= | a -= b | a = a - b | Subtracts `b` from `a` |
// | *= | a *= b | a = a * b | Multiplies `a` by `b` |
// | /= | a /= b | a = a / b | Divides `a` by `b` |
// | %= | a %= b | a = a % b | Finds remainder of `a` divided by `b` |
// | **= | a **= b | a = a ** b | Raises `a` to the power of `b` |

